/*
		= = = = = 
		= = = = 
		= = = 
		= = 
		= 
		* 
		* * 
		* * * 
		* * * * 
		* * * * * 

*/


class Program5 {

	public static void main(String[] args){

		//for loop for row
		for(int row = 1; row <= 5; row++){

			for(int col = 5; col >= row; col--){

				System.out.print("= ");

				}
			
			System.out.println();
			
		}  

		//for loop for row
		for(int row = 1; row <= 5; row++){

			for(int col = 1; col <= row; col++){

				System.out.print("* ");

				}
			
			System.out.println();
			
		}   

		                        

	}


}
