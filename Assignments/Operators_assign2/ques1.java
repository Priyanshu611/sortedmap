class Operators {

	public static void main(String[] args){

		int num1 = 20, num2 = 30;
                boolean ans;

		ans = num1 < num2;
                System.out.println(ans);//true

                ans = num1 > num2;
                System.out.println(ans);//false

                ans = num1 <= num2;
                System.out.println(ans);//true

                ans = num1 >= num2;
                System.out.println(ans);//false

                ans = num1 == num2;
                System.out.println(ans);//false

                ans = num1 != num2;
                System.out.println(ans);//true
    

        }

}
