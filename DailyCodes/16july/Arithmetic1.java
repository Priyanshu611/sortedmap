 class Arithmetic1 {


   public static void main(String[] args) {

          int num1 = 20;
          int num2 = 10;
          int ans = 0;

          ans = num1 + num2;//adition
          System.out.println("Addition is " + ans); //30

          ans = num1 - num2;//subtraction        
          System.out.println("Subtraction is " + ans); //10

          ans = num1 * num2; //multiplcation
          System.out.println("Multiplication is " + ans);//200
 
  
          ans = num1 / num2; //division
          System.out.println("Division is " + ans);  //2

          ans = num1 % num2; //modulus
          System.out.println("Modulus is " + ans); //0

   }

}



